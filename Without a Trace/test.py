# Original integer
integer_value = 504280474484

# Step 1: Convert integer to bytes
num_bytes = (integer_value.bit_length() + 7) // 8
byte_representation = integer_value.to_bytes(num_bytes, 'big')

# Step 2: Convert bytes to string
string_representation = byte_representation.decode('ascii')

print(f"String representation: {string_representation}")


